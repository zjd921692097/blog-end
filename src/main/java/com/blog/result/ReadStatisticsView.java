package com.blog.result;

public class ReadStatisticsView {
    private int countThreeDay;
    private int countWeek;
    private int countMouth;
    private int countYer;
    private String bookName;

    public String getBookName() {
        return bookName;
    }

    public void setBookName(String bookName) {
        this.bookName = bookName;
    }

    public int getCountThreeDay() {
        return countThreeDay;
    }

    public void setCountThreeDay(int countThreeDay) {
        this.countThreeDay = countThreeDay;
    }

    public int getCountWeek() {
        return countWeek;
    }

    public void setCountWeek(int countWeek) {
        this.countWeek = countWeek;
    }

    public int getCountMouth() {
        return countMouth;
    }

    public void setCountMouth(int countMouth) {
        this.countMouth = countMouth;
    }

    public int getCountYer() {
        return countYer;
    }

    public void setCountYer(int countYer) {
        this.countYer = countYer;
    }
}
